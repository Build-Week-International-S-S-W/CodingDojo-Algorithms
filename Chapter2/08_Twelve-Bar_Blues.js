/* Write a function that console.logs the number 1, then "chick", then "boom", then "chick", "boom", "chick" - continuing the same cycle for each number up to (and including) 12. */

for (var i = 1; i <= 12; i++) {
  console.log(i);
  console.log('chick');
  console.log('boom');
  console.log('chick');
  console.log('boom');
  console.log('chick');
}
